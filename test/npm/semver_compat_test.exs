    for {range, version} <- @range_include do
      @range range
      @version version
      test "#{version} satisfies #{range}" do
        assert NPMSemver.matches?(@version, @range),
               "Expected #{@version} to satisfy range #{@range}"
      end
    end
  end

  describe "npm/node-semver range-exclude (ported)" do
    for {range, version} <- @range_exclude do
