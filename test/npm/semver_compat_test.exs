      test "#{version} does NOT satisfy #{range}" do
        refute NPMSemver.matches?(@version, @range),
               "Expected #{@version} to NOT satisfy range #{@range}"
      end
    end
  end

  describe "npm/node-semver comparisons (ported)" do
    for {v1, v2} <- @comparisons do
      @v1 v1
      @v2 v2
      test "#{v1} > #{v2}" do
        assert NPM.VersionUtil.gt?(@v1, @v2),
               "Expected #{@v1} > #{@v2}"
      end

      test "#{v2} < #{v1}" do
        assert NPM.VersionUtil.lt?(@v2, @v1),
               "Expected #{@v2} < #{@v1}"
      end

